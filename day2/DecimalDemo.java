/**
	需求:
	浮点数类型的使用
*/
public class DecimalDemo
{
	public static void main(String[] args)
	{
		//小数常量默认是double类型的
		System.out.println(5.67); //double类型的常量
		System.out.println(5.67F);//float类型的常量
		System.out.println(3.14E2);//314.0

		//小数变量
		//double类型变量
		double a=5.16; 
		//float类型变量
		float b=4.13F;
		System.out.println(a+b);
	}
}
/**
	小结:
	float类型要F 结尾

*/



