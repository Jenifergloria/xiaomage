/**
	需求:
	boolean 类型的使用
*/
public class BooleanDemo
{
	public static void main(String[] args)
	{
		//boolean类型的常量true/false
		System.out.println(true);
		System.out.println(false);

		//boolean类型的变量
		//数据类型
		boolean man=true;
		System.out.println(man);

		//给man重新赋值
		man=false;
		System.out.println(man);
	}
		
}
/**
	小结
	boolean类型的常量只有true/false
*/





