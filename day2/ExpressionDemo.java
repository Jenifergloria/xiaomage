/**
	需求:
	表达式的使用
*/
public class ExpressionDemo
{
	public static void main(String[] args)
	{
		//先声明变量
		int a=2;
		double b=4.15;
		int c=5;
		int d=7;

		//表达式的使用
		System.out.println(a+b);
		System.out.println(8.99+b);
		System.out.println((a*b-c)/2);
		System.out.println(a+3+d);
	}

}
/**
	小结:
	表达式是由常量、变量、运算符、括号组成能求得值的有意义结果的语句
*/











